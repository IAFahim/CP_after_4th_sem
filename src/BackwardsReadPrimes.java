    static boolean isPrime(long n) {
        if (n <= 1)
            return false;
        if (n <= 3)
            return true;
        if (n % 2 == 0 || n % 3 == 0)
            return false;
        for (long i = 5; i * i <= n; i = i + 6)
            if (n % i == 0 || n % (i + 2) == 0)
                return false;
        return true;
    }

    public static long reverse(long n) {
        long rev = 0;
        while (n != 0) {
            rev = rev * 10;
            rev = rev + n % 10;
            n = n / 10;
        }
        return rev;
    }

    public static String backwardsPrime(long start, long end) {

        StringBuilder sb = new StringBuilder();
        for (long i = start; i <= end; i++) {
            long rev=reverse(i);
            if (rev!=i && isPrime(i) && isPrime(rev)) {
                sb.append(i).append(' ');
            }
        }
        if(sb.length()==0){
            return "";
        }

        return sb.substring(0,sb.length()-1).toString();
    }


